              <div class="question-title clearfix">
                <h4>20 岁女生怎么买到有档次而不奢侈的衣服？</h4>
                <div class="question-info clearfix">
                  <span>提问者</span>
                  <div class="pull-right">提问于：2016-03-24 18:00:00</div>
                </div>
              </div>
              <div class="question-detail">
                <p>
                  本人20刚出头，身材很大众微胖160+ 50+，坐标二线省会城市，经济水平小康向上，最近陷入了买衣服困难期。高中时期很喜欢穿依恋家的各个系列衣服，现在买衣服很尴尬，没有办法再穿高中时期不成熟的衣服，不喜欢商场里千篇一律的花花绿绿化纤的时髦的衣服感觉材质很差，尤其是snidel之类的日装卖的很贵但感觉材质比较差，而且还是设计给瘦瘦的小美人穿的（胖子捂脸）。izzue之类的款式有一点太潮流了容易淘汰，容易暴露身材缺点，而且感觉质量也比较一般。淘宝上的材质款式也同样不可信，没有模特的脸蛋身材和气质，而且还没有办法试穿。很多白富美朋友穿奢侈品，但是我觉得没必要也没能力负担那个水平。
                  所以假设买衣服预算（请看清楚了，不是均价，别乱喷，谢谢合作）是一件春装一千元，冬装两三千元水平，由于工作学习的缘故偶尔可以去香港和北美扫货，也可以海淘，去哪里买什么牌子的衣服比较合适呢？能够显气质又不老气，有档次又不到奢侈品的水平？风格不限，可甜美可简约，可日韩可欧美。非常感谢！
                  请一定要帮帮我！这个曾经的购物狂疯女人已经有一两年没有热情没好好买两件衣服了！自信都没有了！对自己的爱都没有了！啊啊啊啊啊啊啊
                </p>
              </div>
